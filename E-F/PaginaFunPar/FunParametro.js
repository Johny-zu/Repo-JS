let resultado;
function suma(valor1, valor2){
    resultado = valor1 + valor2;
    document.write("El resultado de la suma es: " + resultado + "<br>");
}
suma(10, 20); // Lamado a la función

let resultadoResta;
function resta(a,b){
    resultadoResta = a - b;
    return resultadoResta
}
document.write("El resultado de la resta es: " + resta(50, 20)); // Llamado a la función
